import org.degante.arreglos.Arreglo;

public class App {
    public static void main(String[] args) throws Exception {
        Arreglo arreglo = new Arreglo();
        arreglo.sumarElementos();
        arreglo.invertirElementos();
        arreglo.eliminarDuplicados();
        arreglo.eliminarDuplicadosSinHashSet();
        arreglo.numeroMayor();
        arreglo.numeroMenor();
    }
}
